library(shiny)
library(editor)

shinyUI(fluidPage(

  # Application title
  titlePanel("Old Faithful Geyser Data"),

  # Sidebar with a slider input for number of bins
  sidebarLayout(
    sidebarPanel(
      shiny::tableOutput("x")
    ),

    # Show a plot of the generated distribution
    mainPanel(
      editorUI("tabela")
    )
  )
))
